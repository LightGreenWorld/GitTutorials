# 파이썬에서는 py파일을 모듈이라는 개념으로 부른다.
# 모듈은 다른 모듈에 구현한 변수, 함수, 클래스 등을 포함시켜
# 사용할 수 있다.

# sub.py를 포함한다.
import sub        #같은 위치에 있는 sub.py파일을 불러옴
import sub

#sub.py에 정의되어 있는 요소들을 사용한다.
print(f'sub.sub_a1 : {sub.sub_a1}')
sub.sub_function()
